    private void selectData() {
        //To change body of generated methods, choose Tools | Templates.
        String kolom[] = {"NIS","NamaSiswa","TempatLahir", "JenisKelamin", "Kelas", "Email", "Alamat","TanggalLahir"};
        DefaultTableModel dtm = new DefaultTableModel (null, kolom);
        String SQL = "SELECT * FROM t_siswa";
        ResultSet rs;
        try {
            rs = KoneksiDB.executeQuery(SQL);
            while(rs.next()){
                String NIS = rs.getString(1);
                String NamaSiswa = rs.getString(2);
                String TempatLahir = rs.getString(3);
                String JenisKelamin = "";
                if ("L".equals(rs.getString(4))){
                    JenisKelamin = "Laki-laki";
                }else {
                    JenisKelamin = "Perempuan";
                }
                String Kelas = rs.getString(5);
                String Email = rs.getString(6);
                String Alamat = rs.getString(7);
              
                String data[] = {NIS,NamaSiswa,TempatLahir,JenisKelamin,Kelas,Email,Alamat};
                dtm.addRow(data);
            }
            tblData.setModel(dtm);
        } catch (SQLException ex) {
            java.util.logging.Logger.getLogger(frmMain.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        
    }
